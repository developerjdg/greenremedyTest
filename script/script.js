// Function to get visible slides count based on viewport
function getVisibleSlidesCount() {
    const width = window.innerWidth;
    if (width <= 767) return 1;
    if (width <= 900) return 2;
    return 3;
}

// Function to check if scrolling is needed
function isScrollNeeded(container) {
    const slider = container.querySelector('.image-slider');
    const slidesCount = slider.querySelectorAll('.image-slide').length;
    const visibleCount = getVisibleSlidesCount();
    const maxScrollLeft = slider.scrollWidth - slider.clientWidth;

    return maxScrollLeft > 1 && slidesCount > visibleCount;
}

// Function to update arrow states and progress bar
function updateArrowStates(container) {
    const slider = container.querySelector('.image-slider');
    const leftArrow = container.querySelector('.slider-arrow.left');
    const rightArrow = container.querySelector('.slider-arrow.right');
    const progressBar = container.querySelector('.progress');

    requestAnimationFrame(() => {
        const maxScrollLeft = slider.scrollWidth - slider.clientWidth;
        const currentScroll = slider.scrollLeft;
        const slidesCount = slider.querySelectorAll('.image-slide').length;
        const visibleCount = getVisibleSlidesCount();

        // If all slides fit in view (no scrolling needed)
        if (maxScrollLeft <= 1 || slidesCount <= visibleCount) {
            leftArrow.disabled = true;
            rightArrow.disabled = true;
            // Show progress bar as full (100%)
            progressBar.classList.add('full');
        } else {
            // Remove full class and update based on scroll
            progressBar.classList.remove('full');

            // Enable/disable based on scroll position
            leftArrow.disabled = currentScroll <= 1;
            rightArrow.disabled = currentScroll >= maxScrollLeft - 1;

            // Update progress bar width based on scroll position
            let scrollPercentage = (currentScroll / maxScrollLeft) * 100;
            progressBar.style.width = `${Math.max(20, scrollPercentage)}%`;
        }
    });
}

// Function to update all sliders' arrow states
function updateAllArrowStates() {
    document.querySelectorAll('.slider-container').forEach(container => {
        updateArrowStates(container);
    });
}

// Initialize the application
function init() {
    renderTabs();
    renderSliders();
    setupEventListeners();

    // Initial setup with delay for layout
    setTimeout(() => {
        const activeWrapper = document.querySelector('.slider-wrapper.active');
        if (activeWrapper) {
            const container = activeWrapper.querySelector('.slider-container');
            updateArrowStates(container);
            startAutoplay(activeWrapper);
        }
    }, 100);
}

// Render tabs dynamically
function renderTabs() {
    const tabsContainer = document.getElementById('tabs');
    config.categories.forEach((category, index) => {
        const tab = document.createElement('div');
        tab.className = `tab ${index === 0 ? 'active' : ''}`;
        tab.setAttribute('data-category', category.id);
        tab.textContent = category.label;
        tabsContainer.appendChild(tab);
    });
}

// Render all sliders dynamically
function renderSliders() {
    const slidersContainer = document.getElementById('sliders');
    config.categories.forEach((category, index) => {
        const wrapper = createSliderWrapper(category.id, index === 0);
        slidersContainer.appendChild(wrapper);
    });
}

// Create a slider wrapper for a category
function createSliderWrapper(categoryId, isActive) {
    const wrapper = document.createElement('div');
    wrapper.className = `slider-wrapper ${isActive ? 'active' : ''}`;
    wrapper.setAttribute('data-category', categoryId);

    const container = document.createElement('div');
    container.className = 'slider-container';

    // Left arrow
    const leftArrow = document.createElement('button');
    leftArrow.className = 'slider-arrow left';
    leftArrow.setAttribute('aria-label', 'Scroll Left');
    leftArrow.innerHTML = '<i class="ri-arrow-left-line"></i>';

    // Slider
    const slider = document.createElement('div');
    slider.className = 'image-slider';
    slider.id = `${categoryId}Slider`;

    // Add products
    const products = productsData[categoryId] || [];
    products.forEach(product => {
        const slide = createSlide(product);
        slider.appendChild(slide);
    });

    // Right arrow
    const rightArrow = document.createElement('button');
    rightArrow.className = 'slider-arrow right';
    rightArrow.setAttribute('aria-label', 'Scroll Right');
    rightArrow.innerHTML = '<i class="ri-arrow-right-line"></i>';

    // Progress bar
    const progressBar = document.createElement('div');
    progressBar.className = 'progress-bar';
    const progress = document.createElement('div');
    progress.className = 'progress';
    progressBar.appendChild(progress);

    container.appendChild(leftArrow);
    container.appendChild(slider);
    container.appendChild(rightArrow);
    container.appendChild(progressBar);
    wrapper.appendChild(container);

    if (isActive) {
        const firstImg = slider.querySelector('img');
        if (firstImg) {
            firstImg.loading = 'eager';
            firstImg.fetchPriority = 'high';
            firstImg.decoding = 'sync';
        }
    }

    return wrapper;
}

// Create a single slide
function createSlide(product) {
    const slide = document.createElement('div');
    slide.className = 'image-slide';

    const link = document.createElement('a');
    link.href = product.url;

    const img = document.createElement('img');
    img.draggable = false;
    img.src = product.image;
    img.alt = product.name;

    // Performance fixes
    img.loading = 'lazy';
    img.decoding = 'async';
    img.fetchPriority = 'low';

    const overlay = document.createElement('div');
    overlay.className = 'slide-overlay';

    const description = document.createElement('div');
    description.className = 'slide-description';
    description.textContent = config.disclaimer;

    overlay.appendChild(description);
    link.appendChild(img);
    link.appendChild(overlay);
    slide.appendChild(link);

    return slide;
}

