// Tab switching
function setupTabListeners() {
    const tabs = document.querySelectorAll('.tab');
    tabs.forEach(tab => {
        tab.addEventListener('click', () => {
            tabs.forEach(t => t.classList.remove('active'));
            tab.classList.add('active');

            const category = tab.getAttribute('data-category');
            stopAllAutoplay();

            const wrappers = document.querySelectorAll('.slider-wrapper');
            wrappers.forEach(wrapper => {
                wrapper.classList.toggle('active', wrapper.getAttribute('data-category') === category);
            });

            const activeWrapper = document.querySelector('.slider-wrapper.active');
            if (activeWrapper) {
                const container = activeWrapper.querySelector('.slider-container');
                updateArrowStates(container);
                startAutoplay(activeWrapper);
            }
        });
    });
}

// Slider interactions
function setupSliderListeners() {
    document.querySelectorAll('.slider-container').forEach(container => {
        const slider = container.querySelector('.image-slider');
        const progressBar = container.querySelector('.progress');
        const wrapper = container.closest('.slider-wrapper');
        const leftArrow = container.querySelector('.slider-arrow.left');
        const rightArrow = container.querySelector('.slider-arrow.right');

        // Mouse drag
        let isDown = false;
        let startX, scrollLeft;

        slider.addEventListener('mousedown', (e) => {
            isDown = true;
            startX = e.pageX - slider.offsetLeft;
            scrollLeft = slider.scrollLeft;
            pauseAndResumeAutoplay(wrapper);
        });

        slider.addEventListener('mouseleave', () => isDown = false);
        slider.addEventListener('mouseup', () => isDown = false);

        slider.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - slider.offsetLeft;
            const walk = (x - startX) * 2;
            slider.scrollLeft = scrollLeft - walk;
        });

        // Touch support
        let touchStartX = 0;
        let touchScrollLeft = 0;

        slider.addEventListener('touchstart', (e) => {
            touchStartX = e.touches[0].pageX - slider.offsetLeft;
            touchScrollLeft = slider.scrollLeft;
            pauseAndResumeAutoplay(wrapper);
        });

        slider.addEventListener('touchmove', (e) => {
            if (!touchStartX) return;
            const x = e.touches[0].pageX - slider.offsetLeft;
            const walk = (x - touchStartX) * 2;
            slider.scrollLeft = touchScrollLeft - walk;
        });

        slider.addEventListener('touchend', () => touchStartX = 0);

        // Progress bar and arrow states on scroll
        slider.addEventListener('scroll', () => {
            // Only update progress if scrolling is needed
            if (isScrollNeeded(container)) {
                const maxScrollLeft = slider.scrollWidth - slider.clientWidth;
                let scrollPercentage = (slider.scrollLeft / maxScrollLeft) * 100;
                if (scrollPercentage < 20) scrollPercentage = 20;
                progressBar.style.width = `${scrollPercentage}%`;
            }

            // Update arrow states on scroll
            updateArrowStates(container);
        });

        // Arrows with disabled check
        leftArrow.addEventListener('click', () => {
            if (leftArrow.disabled) return;
            const slideWidth = slider.querySelector('.image-slide').offsetWidth + 20;
            slider.scrollBy({
                left: -slideWidth,
                behavior: 'smooth'
            });
            pauseAndResumeAutoplay(wrapper);
        });

        rightArrow.addEventListener('click', () => {
            if (rightArrow.disabled) return;
            const slideWidth = slider.querySelector('.image-slide').offsetWidth + 20;
            slider.scrollBy({
                left: slideWidth,
                behavior: 'smooth'
            });
            pauseAndResumeAutoplay(wrapper);
        });

        // Hover pause
        slider.addEventListener('mouseenter', () => pauseAndResumeAutoplay(wrapper));

        // Initial arrow state update after images load
        const images = slider.querySelectorAll('img');
        let loadedCount = 0;
        images.forEach(img => {
            if (img.complete) {
                loadedCount++;
                if (loadedCount === images.length) {
                    updateArrowStates(container);
                }
            } else {
                img.addEventListener('load', () => {
                    loadedCount++;
                    if (loadedCount === images.length) {
                        updateArrowStates(container);
                    }
                });
            }
        });
    });
}

// Keyboard navigation
function setupKeyboardNavigation() {
    document.addEventListener('keydown', (e) => {
        const activeWrapper = document.querySelector('.slider-wrapper.active');
        if (!activeWrapper) return;

        const activeSlider = activeWrapper.querySelector('.image-slider');
        const container = activeWrapper.querySelector('.slider-container');
        const leftArrow = container.querySelector('.slider-arrow.left');
        const rightArrow = container.querySelector('.slider-arrow.right');

        if (activeSlider) {
            const slideWidth = activeSlider.querySelector('.image-slide').offsetWidth + 20;
            if (e.key === 'ArrowLeft' && !leftArrow.disabled) {
                activeSlider.scrollBy({
                    left: -slideWidth,
                    behavior: 'smooth'
                });
                pauseAndResumeAutoplay(activeWrapper);
            } else if (e.key === 'ArrowRight' && !rightArrow.disabled) {
                activeSlider.scrollBy({
                    left: slideWidth,
                    behavior: 'smooth'
                });
                pauseAndResumeAutoplay(activeWrapper);
            }
        }
    });
}

// Visibility change
function setupVisibilityChange() {
    document.addEventListener('visibilitychange', () => {
        if (document.hidden) {
            stopAllAutoplay();
        } else {
            const activeWrapper = document.querySelector('.slider-wrapper.active');
            if (activeWrapper) {
                startAutoplay(activeWrapper);
            }
        }
    });
}

// Resize listener
function setupResizeListener() {
    window.addEventListener('resize', () => {
        updateAllArrowStates();
    });
}

// Autoplay functions
function startAutoplay(wrapper) {
    if (!wrapper) return;

    const slider = wrapper.querySelector('.image-slider');
    const container = wrapper.querySelector('.slider-container');
    const category = wrapper.getAttribute('data-category');

    // Don't start autoplay if all slides are visible
    if (!isScrollNeeded(container)) {
        return;
    }

    if (autoplayIntervals.has(category)) {
        clearInterval(autoplayIntervals.get(category));
    }

    const interval = setInterval(() => {
        autoScrollSlider(slider);
    }, config.autoplayInterval);

    autoplayIntervals.set(category, interval);
}

function stopAutoplay(wrapper) {
    const category = wrapper.getAttribute('data-category');
    if (autoplayIntervals.has(category)) {
        clearInterval(autoplayIntervals.get(category));
        autoplayIntervals.delete(category);
    }
}

function stopAllAutoplay() {
    autoplayIntervals.forEach((interval) => clearInterval(interval));
    autoplayIntervals.clear();
    pauseTimeouts.forEach((timeout) => clearTimeout(timeout));
    pauseTimeouts.clear();
}

function pauseAndResumeAutoplay(wrapper) {
    stopAutoplay(wrapper);
    const category = wrapper.getAttribute('data-category');

    if (pauseTimeouts.has(category)) {
        clearTimeout(pauseTimeouts.get(category));
    }

    const timeout = setTimeout(() => {
        if (wrapper.classList.contains('active')) {
            startAutoplay(wrapper);
        }
    }, config.pauseDuration);

    pauseTimeouts.set(category, timeout);
}

function autoScrollSlider(slider) {
    const slideWidth = slider.querySelector('.image-slide').offsetWidth + 20;
    const maxScrollLeft = slider.scrollWidth - slider.clientWidth;

    if (slider.scrollLeft >= maxScrollLeft - 10) {
        slider.scrollTo({
            left: 0,
            behavior: 'smooth'
        });
    } else {
        slider.scrollBy({
            left: slideWidth,
            behavior: 'smooth'
        });
    }
}

